#include <stdio.h>

int main(void){
	int a = 9;
	int b = 016;
	int c = 0x12;
	char *str1 = "!!!hello!!!";
	float f = 3.141592, f2 = 0.00001234;

	printf("-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+\n");
	printf("%+d\n", a);/* +9 */
	printf("%*d%*o |\n", -10, a, -10, b);/* 9         16         | */
	printf("%#o %#x\n", b, c);/* 016 0x12 */
	printf("%o %x\n", b, c);/* 16 12 */

	printf("%.10d\n", a);/* 0000000009 */
	printf("%.10f\n", f);/* 3.1415920258 */ /* 小数点后7 8 9 10位似乎并无意义. */
	printf("%.10g  |%.10g|\n", f, f2);/* 3.141592026  |1.233999956e-005| */
	printf("|%.2g|\n", f2);/* |1.2e-005| */
	printf("|%.5s|\n", str1);/* |!!!he| */ /* 字符串长度大于这里的最大字节数，那么字符串被截断。 */
	
	return 0;
}