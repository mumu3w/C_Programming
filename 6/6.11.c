/******************************************************************************
  题目: 数学常量e的值可以用一个无穷级数表示：
		e = 1+1/1!+1/2!+...
		编写程序,用下列公式计算e的近似值：
		e = 1+1/1!+1/2!+...+1/n!
		
  演示: Enter a number: 6
		e: 2.718056
		----------------------------------------------------------------------
		Enter a number: 7
		e: 2.718254
		----------------------------------------------------------------------
		Enter a number: 8
		e: 2.718279
		----------------------------------------------------------------------
		Enter a number: 9
		e: 2.718282
		
  邮箱：mumu3w@outlook.com	
  
  时间：10/19/2015 
  
******************************************************************************/

#include <stdio.h>

int main(){
	int i1, i2, n;
	long denom;
	float e;
	
	printf("Enter a number: ");
	scanf("%d", &n);

	e = 1.00f;
	for(i1 = 1; i1 <= n; i1++){
		denom = 1;
		
		for(i2 = 1; i2 <= i1; i2++){
			denom *= i2;
		}
		e = e + 1.00f/denom;
	}
	
	printf("e: %f", e);
	
	return 0;
}

